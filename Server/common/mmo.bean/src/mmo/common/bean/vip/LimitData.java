package mmo.common.bean.vip;

public class LimitData {
	private String funDiscribe = null; // 功能描述
	private String strValue    = null;
	private int    defaultCount;      // 默认数量
	private int    lingShiCount;      // 灵石数量
	private int    expGrass;          // 经验草数量
	private int    findTreasure;      // 寻宝牌数量
	private int    heroCount;         // 英雄牌数量
	private int    callCount;         // 召唤牌数量
	private int    redStone;          // 红宝石数量
	private int    yellowStone;       // 黄宝石数量
	private int    blueStone;         // 蓝宝石数量

	public String getFunDiscribe() {
		return funDiscribe;
	}

	public void setFunDiscribe(String funDiscribe) {
		this.funDiscribe = funDiscribe;
	}

	public String getStrValue() {
		return strValue;
	}

	public void setStrValue(String strValue) {
		this.strValue = strValue;
	}

	public int getDefaultCount() {
		return defaultCount;
	}

	public void setDefaultCount(int defaultCount) {
		this.defaultCount = defaultCount;
	}

	public int getLingShiCount() {
		return lingShiCount;
	}

	public void setLingShiCount(int lingShiCount) {
		this.lingShiCount = lingShiCount;
	}

	public int getExpGrass() {
		return expGrass;
	}

	public void setExpGrass(int expGrass) {
		this.expGrass = expGrass;
	}

	public int getFindTreasure() {
		return findTreasure;
	}

	public void setFindTreasure(int findTreasure) {
		this.findTreasure = findTreasure;
	}

	public int getHeroCount() {
		return heroCount;
	}

	public void setHeroCount(int heroCount) {
		this.heroCount = heroCount;
	}

	public int getCallCount() {
		return callCount;
	}

	public void setCallCount(int callCount) {
		this.callCount = callCount;
	}

	public int getRedStone() {
		return redStone;
	}

	public void setRedStone(int redStone) {
		this.redStone = redStone;
	}

	public int getYellowStone() {
		return yellowStone;
	}

	public void setYellowStone(int yellowStone) {
		this.yellowStone = yellowStone;
	}

	public int getBlueStone() {
		return blueStone;
	}

	public void setBlueStone(int blueStone) {
		this.blueStone = blueStone;
	}
}
