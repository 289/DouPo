	public InstPlayerYFire add(final InstPlayerYFire model, int instPlayerId) throws Exception {
		try {
			final String  updateTime = new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss").format(System.currentTimeMillis());
			StringBuilder strSql = new StringBuilder();
			strSql.append(" insert into Inst_Player_YFire (");
			strSql.append("instPlayerId,fireId,state,fireTime,hp,speed,owner,chipCount,insertTime,updateTime,version");
			strSql.append(" )");
			strSql.append(" values (?,?,?,?,?,?,?,?,?,?,?) ");

			final String sql = strSql.toString();
			KeyHolder keyHolder = new GeneratedKeyHolder();

			this.getJdbcTemplate().update(new PreparedStatementCreator(){
				public PreparedStatement createPreparedStatement(Connection conn) throws SQLException {
					PreparedStatement ps = conn.prepareStatement(sql);
					ps.setInt(1, model.getInstPlayerId());
					ps.setInt(2, model.getFireId());
					ps.setInt(3, model.getState());
					ps.setString(4, model.getFireTime());
					ps.setInt(5, model.getHp());
					ps.setInt(6, model.getSpeed());
					ps.setString(7, model.getOwner());
					ps.setInt(8, model.getChipCount());
					ps.setString(9, updateTime);
					ps.setString(10, updateTime);
					ps.setInt(11, 0);
					return ps;
				}
			},keyHolder);

			model.setId(keyHolder.getKey().intValue());
			model.setVersion(0);
			model.setInsertTime(updateTime, 0);
			model.setUpdateTime(updateTime, 0);
			PlayerMemObj playerMemObj = getPlayerMemObjByPlayerId(instPlayerId);
			if (instPlayerId != 0 && isUseCach() && playerMemObj != null) {
				playerMemObj.instPlayerYFireMap.put(model.getId(), model);
			}
		} catch (Exception e) {
			throw e;
		}
		return model;
	}

