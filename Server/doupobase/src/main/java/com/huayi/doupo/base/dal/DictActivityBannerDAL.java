	public DictActivityBanner add(final DictActivityBanner model, int instPlayerId) throws Exception {
		try {
			StringBuilder strSql = new StringBuilder();
			strSql.append(" insert into Dict_Activity_Banner (");
			strSql.append("title,ui,isInActivity,activityId,skip,rank,description,version");
			strSql.append(" )");
			strSql.append(" values (?,?,?,?,?,?,?,?) ");

			final String sql = strSql.toString();
			KeyHolder keyHolder = new GeneratedKeyHolder();

			this.getJdbcTemplate().update(new PreparedStatementCreator(){
				public PreparedStatement createPreparedStatement(Connection conn) throws SQLException {
					PreparedStatement ps = conn.prepareStatement(sql);
					ps.setString(1, model.getTitle());
					ps.setString(2, model.getUi());
					ps.setInt(3, model.getIsInActivity());
					ps.setInt(4, model.getActivityId());
					ps.setString(5, model.getSkip());
					ps.setString(6, model.getRank());
					ps.setString(7, model.getDescription());
					ps.setInt(8, 0);
					return ps;
				}
			},keyHolder);

			model.setId(keyHolder.getKey().intValue());
			model.setVersion(0);
			PlayerMemObj playerMemObj = getPlayerMemObjByPlayerId(instPlayerId);
			if (instPlayerId != 0 && isUseCach() && playerMemObj != null) {
				playerMemObj.dictActivityBannerMap.put(model.getId(), model);
			}
		} catch (Exception e) {
			throw e;
		}
		return model;
	}

	public void batchAdd (final List<DictActivityBanner> list) {
		StringBuilder sql = new StringBuilder();
		sql.append(" insert into Dict_Activity_Banner (");
		sql.append("title,ui,isInActivity,activityId,skip,rank,description,version");
		sql.append(" )");
		sql.append(" values (?,?,?,?,?,?,?,?) ");
		BatchPreparedStatementSetter setter = new BatchPreparedStatementSetter (){
			public void setValues(PreparedStatement ps, int i) throws SQLException{
				DictActivityBanner model = (DictActivityBanner)list.get(i);
					ps.setString(1, model.getTitle());
					ps.setString(2, model.getUi());
					ps.setInt(3, model.getIsInActivity());
					ps.setInt(4, model.getActivityId());
					ps.setString(5, model.getSkip());
					ps.setString(6, model.getRank());
					ps.setString(7, model.getDescription());
					ps.setInt(8, 0);
			}
			public int getBatchSize(){
				return list.size();
			}
		};
		getJdbcTemplate().batchUpdate(sql.toString(), setter);
	}

