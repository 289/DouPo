	public DictActivityBanner update(DictActivityBanner model, int instPlayerId) throws Exception {
		try {
			Object[] params = null;
			int version = model.getVersion() + 1;
			StringBuffer sql = new StringBuffer("update Dict_Activity_Banner set ");
			sql.append("title=?,ui=?,isInActivity=?,activityId=?,skip=?,rank=?,description=?,version=? ");
			sql.append("where id=? and version=?");
			params = new Object[] { model.getTitle(),model.getUi(),model.getIsInActivity(),model.getActivityId(),model.getSkip(),model.getRank(),model.getDescription(),version , model.getId(), model.getVersion() };
			int count = this.getJdbcTemplate().update(sql.toString(), params);
			if (count == 0) {
				throw new Exception("Concurrent Exception");
			} else {
				model.setVersion(version, 0);
				PlayerMemObj playerMemObj = getPlayerMemObjByPlayerId(instPlayerId);
				if (instPlayerId != 0 && isUseCach() && playerMemObj != null) {
					playerMemObj.dictActivityBannerMap.put(model.getId(), model);
				}
			}
		} catch (Exception e) {
			throw e;
		}
		return model;
	}

	@SuppressWarnings("unchecked")
	public DictActivityBanner getModel(int id, int instPlayerId) {
		try {
			PlayerMemObj playerMemObj = getPlayerMemObjByPlayerId(instPlayerId);
			if (instPlayerId != 0 && isUseCach() && playerMemObj != null) {
				DictActivityBanner model = playerMemObj.dictActivityBannerMap.get(id);
				if (model == null) {
					String sql = "select * from Dict_Activity_Banner where id=?";
					Object[] params = new Object[]{id};
					playerMemObj.dictActivityBannerMap.put(id, (DictActivityBanner) this.getJdbcTemplate().queryForObject(sql, params, new ItemMapper()));
				} else {
					int cacheVersion = model.getVersion();
					List<Map<String, Object>> list = sqlHelper("select version from Dict_Activity_Banner where id = " + id);
					 int dbVersion = (int)list.get(0).get("version");
					if (cacheVersion != dbVersion) {
						String sql = "select * from Dict_Activity_Banner where id=?";
						Object[] params = new Object[]{id};
						playerMemObj.dictActivityBannerMap.put(id, (DictActivityBanner) this.getJdbcTemplate().queryForObject(sql, params, new ItemMapper()));
					}
				}
				model = playerMemObj.dictActivityBannerMap.get(id);
				model.result = "";
				return model;
			} else {
				String sql = "select * from Dict_Activity_Banner where id=?";
				Object[] params = new Object[]{id};
				DictActivityBanner model = ( DictActivityBanner) this.getJdbcTemplate().queryForObject(sql, params, new ItemMapper());
				model.result = "";
				return model;
			}
		} catch (DataAccessException e) {
			return null;
		}
	}

